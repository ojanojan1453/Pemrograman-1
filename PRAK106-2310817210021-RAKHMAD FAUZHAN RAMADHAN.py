a = 4
b = 8
c = 3

print(f"Variabel a bernilai {a}")
print(f"Variabel b bernilai {b}")
print(f"Variabel c bernilai {c}")

hasil_sama_dengan = 0 if a == b else 1

hasil_lebih_besar = 1 if b > c else 0

hasil_tidak_sama_dengan = 1 if a != c else 0

print(f"Apakah a sama dengan b ? jawabannya adalah {hasil_sama_dengan}")
print(f"Apakah b lebih besar dari c ? jawabannya adalah {hasil_lebih_besar}")
print(f"Apakah a tidak sama dengan c ? jawabannya adalah {hasil_tidak_sama_dengan}")
