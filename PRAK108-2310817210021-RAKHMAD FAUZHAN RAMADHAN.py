jumlah_putaran = 5
jarak_tempuh = 14.0

keliling_taman = jarak_tempuh / jumlah_putaran

jari_jari_taman = keliling_taman / (2 * 3.14159)

print("Diketahui :")
print(f"Pak Dengklek mengelilingi taman = {jumlah_putaran} Putaran")
print(f"Jarak tempuh Pak Dengklek = {jarak_tempuh:.2f} Kilometer")
print("Jawaban :")
print(f"Jari-jari taman yang dikelilingi Pak Dengklek adalah {jari_jari_taman:.2f} Kilometer")
